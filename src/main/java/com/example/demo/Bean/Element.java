package com.example.demo.Bean;

public class Element {

    private String elementNumber;
    private String elmentuser;
    private double currentelement;
    private String chargeUnit;
    private String chargeMoeny;

    public String getChargeMoeny() {
        return chargeMoeny;
    }

    public void setChargeMoeny(String chargeMoeny) {
        this.chargeMoeny = chargeMoeny;
    }

    public String getElementNumber() {
        return elementNumber;
    }

    public void setElementNumber(String elementNumber) {
        this.elementNumber = elementNumber;
    }

    public String getElmentuser() {
        return elmentuser;
    }

    public void setElmentuser(String elmentuser) {
        this.elmentuser = elmentuser;
    }

    public double getCurrentelement() {
        return currentelement;
    }

    public void setCurrentelement(double currentelement) {
        this.currentelement = currentelement;
    }

    public String getChargeUnit() {
        return chargeUnit;
    }

    public void setChargeUnit(String chargeUnit) {
        this.chargeUnit = chargeUnit;
    }
}
