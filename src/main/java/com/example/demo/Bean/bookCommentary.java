package com.example.demo.Bean;

public class bookCommentary {
    private String book_name;
    private String stu_account;
    private double stu_score;
    private String stu_comment;

    public String getBook_name() {
        return book_name;
    }

    public void setBook_name(String book_name) {
        this.book_name = book_name;
    }

    public String getStu_account() {
        return stu_account;
    }

    public void setStu_account(String stu_account) {
        this.stu_account = stu_account;
    }

    public double getStu_score() {
        return stu_score;
    }

    public void setStu_score(double stu_score) {
        this.stu_score = stu_score;
    }

    public String getStu_comment() {
        return stu_comment;
    }

    public void setStu_comment(String stu_comment) {
        this.stu_comment = stu_comment;
    }

}
